package com.yondervision.mi.common;

public class ChannelFlow {
	public String channel = "";
	public int countUser = 0;
	public int countBusinesses = 0;
	
	
	public String getChannel() {
		return channel;
	}
	public void setChannel(String channel) {
		this.channel = channel;
	}
	public int getCountUser() {
		return countUser;
	}
	public void setCountUser(int countUser) {
		this.countUser = countUser;
	}
	public int getCountBusinesses() {
		return countBusinesses;
	}
	public void setCountBusinesses(int countBusinesses) {
		this.countBusinesses = countBusinesses;
	}
	
}
