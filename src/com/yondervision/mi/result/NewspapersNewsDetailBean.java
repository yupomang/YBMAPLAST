package com.yondervision.mi.result;

public class NewspapersNewsDetailBean {
	
	private String title = "";
	private String citedtitle = "";
	private String subtopics = "";
	private String source = "";
	private String content = "";
	private int praisecounts = 0;
	private String praiseFlg = "";
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getCitedtitle() {
		return citedtitle;
	}
	public void setCitedtitle(String citedtitle) {
		this.citedtitle = citedtitle;
	}
	public String getSubtopics() {
		return subtopics;
	}
	public void setSubtopics(String subtopics) {
		this.subtopics = subtopics;
	}
	public String getSource() {
		return source;
	}
	public void setSource(String source) {
		this.source = source;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public int getPraisecounts() {
		return praisecounts;
	}
	public void setPraisecounts(int praisecounts) {
		this.praisecounts = praisecounts;
	}
	public String getPraiseFlg() {
		return praiseFlg;
	}
	public void setPraiseFlg(String praiseFlg) {
		this.praiseFlg = praiseFlg;
	}
}
