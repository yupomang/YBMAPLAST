package com.yondervision.mi.result;

public class NewspapersNewsCommentBean {
	private String commentSeqno = "";
	private String commentUser = "";
	private String commentTime = "";
	private String commentCotent = "";
	private int praisecounts = 0;
	private String commentUserId = "";
	private String praiseFlg = "";
	
	public String getCommentSeqno() {
		return commentSeqno;
	}
	public void setCommentSeqno(String commentSeqno) {
		this.commentSeqno = commentSeqno;
	}
	public String getCommentUser() {
		return commentUser;
	}
	public void setCommentUser(String commentUser) {
		this.commentUser = commentUser;
	}
	public String getCommentTime() {
		return commentTime;
	}
	public void setCommentTime(String commentTime) {
		this.commentTime = commentTime;
	}
	public String getCommentCotent() {
		return commentCotent;
	}
	public void setCommentCotent(String commentCotent) {
		this.commentCotent = commentCotent;
	}
	public int getPraisecounts() {
		return praisecounts;
	}
	public void setPraisecounts(int praisecounts) {
		this.praisecounts = praisecounts;
	}
	public String getCommentUserId() {
		return commentUserId;
	}
	public void setCommentUserId(String commentUserId) {
		this.commentUserId = commentUserId;
	}
	public String getPraiseFlg() {
		return praiseFlg;
	}
	public void setPraiseFlg(String praiseFlg) {
		this.praiseFlg = praiseFlg;
	}
	
}
